# For Loops 

for i in range(6):
    print(i+1)

# set the range of loop
print("Set the Range")
for j in range(3,10):
   print(j)

# Loops with list 
print("Loops with List ")
Py_list = [1,2,3,4,5,2,6]

for i in Py_list:
    print(i)

# Loops with Set
print("Loops with List ")
Py_Set = {1,2,3,4,5,2,6}

# Contains only unique value and Order does not manner
# Set does not repeated value 
for item in Py_Set:
    print(item)

